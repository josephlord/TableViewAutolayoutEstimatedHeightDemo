-(void)insertCell
{
    _cellCount++;
    NSIndexPath * newPath = [NSIndexPath indexPathForRow:_cellCount - 1 inSection:0];
    [self.tableView insertRowsAtIndexPaths:@[newPath] withRowAnimation:UITableViewRowAnimationAutomatic];
}

